# BASIC BUILTIN Functions
str("5")
'5'

str("5.6")
'5.6'

str("true")
'true'

bool("true")
True

float("5.7")
5.7

len("hello")
5

len([1, 2, 3, 4])
4

len(["hello", "nick"])
2



sorted([34, 20, 1, 5, 3])
[1, 3, 5, 20, 34]

sorted(["G", "F", "E", "T"])
['E', 'F', 'G', 'T']

sorted(["a", "A", "D", "B", "1", "4", "2"])
['1', '2', '4', 'A', 'B', 'D', 'a']
